      <p> Um dos mais influentes escritores portugueses, Camilo Castelo Branco nasceu em Lisboa no dia 16 de Março de 1825 e ao longo de sua vida contribuiu como historiador, tradutor, romancista, cronista, dramaturgo e crítico. O Rei D. Luís ainda concedeu a ele o título de 1º visconde de Correia Botelho. Camilo escrevia para o público, diferentemente da maioria dos escritores da época que preferiam escrever algo mais pessoal, e acabou se sujeitando aos ditames da moda naquele tempo, mas apesar disso ele conseguiu manter as suas publicações bastante originais.</p>

<p>Batizado com o nome de Camilo Ferreira Botelho Castelo Branco, o escritor ficou órfão de mãe com apenas um ano de idade e seu pai veio a falecer quando ele tinha 10 anos. Com apenas 16 anos, em 1841, Camilo se casou com Joaquina Pereira, contudo acaba por deixar sua esposa em um breve período de tempo. No ano de 1843, Castelo Branco ingressou na Escola Médico-Cirúrgica na cidade do Porto, porém não chegou a concluir o curso, afinal a sua paixão estava mesmo na escrita, tanto que acabou vivendo exclusivamente de seus trabalhos. O ano de 1845 foi um grande marco para o escritor, pois foi quando as suas primeiras obras literárias foram publicadas. Cinco anos depois ele passa por uma grande crise espiritual e ingressa no seminário do Porto, tentando seguir a vida religiosa, mas foi nesse ano que ele conheceu Ana Plácido, que era casada com um comerciante, e apaixonou-se, fazendo-a abandonar o marido em 1859. No início da década de 1860, Camilo foi processado e preso por crime de adultério, mas acaba sendo absolvido no ano seguinte e passa a viver com Ana. Em 1889 as obras de Castelo Branco acabam ganhando mais reconhecimento e ele recebe uma homenagem da Academia de Lisboa.</p>

<p>Mesmo com o reconhecimento dos seus escritos, Camilo Castelo Branco estava depressivo, pois havia contraído uma doença nos olhos que aos poucos lhe tirava a visão. Logo após o diagnóstico definitivo de que iria ficar cego, ele comete suicídio em São Miguel de Seide no dia 1 de junho de 1890.</p>


