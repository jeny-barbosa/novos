    <td> 

      <p>Antônio Frederico de Castro Alves foi um importante poeta brasileiro do século XIX. Nasceu na cidade de Curralinho (Bahia) em 14 de março de 1847.</p>

<p>No período em que viveu (1847-1871), ainda existia a escravidão no Brasil. O jovem baiano, simpático e gentil, apesar de possuir gosto sofisticado para roupas e de levar uma vida relativamente confortável, foi capaz de compreender as dificuldades dos negros escravizados.</p>

<p>Manifestou toda sua sensibilidade escrevendo versos de protesto contra a situação a qual os negros eram submetidos. Este seu estilo contestador o tornou conhecido como o “Poeta dos Escravos”.</p>

<p>Aos 21 anos de idade, mostrou toda sua coragem ao recitar, durante uma comemoração cívica, o “Navio Negreiro”. A contra gosto, os fazendeiros ouviram-no clamar versos que denunciavam os maus tratos aos quais os negros eram submetidos. </p>

<p>Além de poesia de caráter social, este grande escritor também escreveu versos lírico-amorosos, de acordo com o estilo de Vítor Hugo. Pode-se dizer que Castro Alves foi um poeta de transição entre o Romantismo e o Parnasianismo.</p>
<p>Este notável escritor morreu ainda jovem, antes mesmo de terminar o curso de Direito que iniciara, pois, vinha sofrendo de tuberculose desde os seus 16 anos.</p>
<p>Apesar de ter vivido tão pouco, este artista notável deixou livros e poemas significativos.
</p>
