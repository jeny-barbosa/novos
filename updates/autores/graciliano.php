      <p>Graciliano Ramos é considerado um dos mais importantes escritores do moderno romance brasileiro. Nascido em Quebrângulo – AL (1892), morreu no Rio de Janeiro (1953). Estudou em Maceió, e não chegou a cursar uma faculdade. Morou por muito tempo em Palmeira dos Índios – AL, onde seu pai mantinha um comércio.</p>

<p>Graciliano chegou a ser prefeito de Palmeira dos Índios. Dedicando-se ao jornalismo e à publicidade, foi revisor de jornais no Rio de Janeiro e dirigiu a imprensa e a instrução do estado de Alagoas de 1930 a 1936, sempre demonstrando preocupação com o ensino no Brasil. Foi preso em 1936, sob a suspeita de ligação com o Partido Comunista Brasileiro, sendo humilhado dentro dos presídios por onde passou. Em 1945, filia-se ao Comunismo, viajando por vários países socialistas.
</p>

<p>No início dos anos 50, já consagrado como romancista, falece de câncer na capital carioca.
