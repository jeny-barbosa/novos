      <p>Bernardo Guimarães (1825-1884) foi um romancista e poeta brasileiro. "A Escrava Isaura" foi o seu romance mais popular. Estudou Direito em São Paulo. Foi juiz municipal na cidade de Catalão em Goiás. Foi jornalista, professor de latim, francês, retórica e poética. Estreou como poeta com "Cantos da Solidão", mas foi como romancista que seu nome ganhou destaque. Foi considerado o criador do romance sertanejo e regional, ambientado em Minas Gerais e Goiás. De todos os seus romances "O Seminarista" é considerado sua melhor obra. É patrono da cadeira nº 5 da Academia Brasileira de Letras.</p>

<p>Bernardo Guimarães (1825-1884) nasceu no dia 15 de agosto, na cidade de Ouro Preto, em Minas Gerais. Filho de João Joaquim da Silva Guimarães e Constança Beatriz de Oliveira Guimarães. Estudou no seminário e aos 22 anos ingressou na Faculdade de Direito de São Paulo. Foi amigo de Álvares de Azevedo e de Aureliano Lessa. Formou-se em 1852.</p>

<p>Seu primeiro livro publicado foi "Cantos da Solidão", obra poética identificada com sua fama de boêmio e satírico, ainda na faculdade. Logo depois de formado foi para Catalão, em Goiás, onde exerceu o cargo de juiz municipal e lá permaneceu de 1832 até 1854. </p>

<p>Bernardo Guimarães foi morar no Rio de Janeiro em 1858, onde trabalhou como jornalista e crítico literário, no Jornal Atualidades. Em 1861 volta para Catalão, onde reassume o cargo de juiz municipal. Em 1866 é nomeado professor de retórica e poética no Liceu Mineiro de Ouro Preto.</p>

<p>Bernardo Guimarães publica em 1872, seu romance "O Seminarista", onde expõe sua crítica ao celibato religioso. É considerada sua melhor obra. Em 1873 leciona latim e francês na cidade de Queluz em Minas Gerais. É Patrono da cadeira nº5 da Academia Brasileira de Letras e Patrono da cadeira nº15 da Academia Mineira de Letras.</p>
<p>Seu romance mais popular foi "A Escrava Isaura", publicado em 1875, foi reproduzido para a televisão, com grande sucesso e levado para mais de 150 países. O romance conta o amor de Isaura, uma escrava branca, e Álvaro um jovem abolicionista e republicano.</p>
<p>Bernardo Joaquim da Silva Guimarães faleceu no dia 10 de março de 1884, em Ouro Preto, Minas gerais.</p>
