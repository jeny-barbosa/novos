    <td> 
      <p>O médico e escritor Joaquim Manuel de Macedo, nasceu em 24 de junho de 1820 em Itaboraí, RJ, e faleceu em 11 de abril de 1882, no Rio de Janeiro, RJ.
Filho de Severino de Macedo Carvalho e Benigna Catarina da Conceição, Macedo foi criado em sua terra natal, onde concluiu os estudos secundários. Mudou-se, em 1838, para a cidade do Rio de Janeiro, onde ingressou na Faculdade de Medicina.</p>
<p>Formou-se em 1844, nesse mesmo ano publicou A Moreninha, essa obra marca o início da ficção do romantismo brasileiro. Exerceu a profissão de médico por algum tempo no interior do estado do Rio. Sua primeira obra rendeu-lhe fama de forma tão intensa que o levou a abrir mão da carreira médica para dedicar-se exclusivamente à literatura e ao jornalismo.
No ano de 1849, fundou a revista Guanabara, junto com Araújo Porto-Alegre e Gonçalves Dias.
Nessa revista, trechos de seu poema-romance A Nebulosa foram publicados, obra considerada por muitos como uma das melhores do Romantismo.</p>
<p>Foi membro do Conselho Diretor da Instituição Pública da Corte (1866) e professor de História e Geografia do Brasil, no Colégio Dom Pedro II, ligado à família de Dom Pedro II e à família Imperial Brasileira foi preceptor e professor dos filhos da Princesa Isabel.
</p>
<p>Na década de 1850, fundou o jornal A Nação do qual tornou-se o principal articulista. Ligado ao Partido Liberal, se elegeu deputado federal provincial (1854-1859) e geral (1864-1868 / 1878-1881).
</p>
<p>Nos últimos anos de vida foi afligido por problemas mentais, morreu antes de completar 62 anos.
</p>
