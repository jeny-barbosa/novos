 <h1>José de Alencar</h1>
<table>
  <th>
    <td>
      <img src="img/alencar.jpg" width="400" height="500" class="foto">
    </td>
    <td>
      <p>O escritor brasileiro José de Alencar nasceu no Ceará, região nordeste do Brasil, no ano de 1829. Antes de iniciar sua vida literária, atuou como advogado, jornalista, deputado e ministro da justiça. Aos 26 anos publicou sua primeira obra: “Cinco Minutos”. </p>

<p>Podemos considerar Alencar como o precursor do romantismo no Brasil dentro das quatro características: indianista, psicológico, regional e histórico. </p>

<p>Este autor brasileiro utilizou como tema o índio e o sertão do Brasil e, ao contrário de outros romancistas de sua época que escreviam com se vivessem em Portugal, Alencar valorizava a língua falada no Brasil. </p>

<p>Escritor de obras com estilos variados, este escritor cearense criou romances que abordam o cotidiano. Deste estilo literário, também conhecido como romance de costumes, destacam-se os livros: Diva, Lucíola e A Viuvinha. Foram também de sua autoria os romances regionalistas: O Sertanejo, O Tronco do Ipê, O Gaúcho e Til. Dos romances históricos fazem parte: As Minas de Prata e A Guerra dos Mascates. </p>

<p>No romance indianista de José de Alencar, o índio é visto em três etapas diferentes: antes de ter contato com o branco, em Ubirajara; um branco convivendo no meio indígena, em Iracema e o índio no cotidiano do homem branco, em O Guarani.</p>

<p>É dentro do estilo indianista do escritor José de Alencar que está sua obra mais importante: Iracema. Outra obra também considerada de grande valor literário é O Guarani, pois aborda os aspectos da formação nacional brasileira. </p>

<p>Apesar de ser mais conhecido por suas obras literárias, o escritor brasileiro José de Alencar fez também algumas peças de teatro: Nas Asas de um Anjo, Mãe, O Demônio Familiar. </p>

<p>Faleceu aos 48 anos de idade, em 1877, deixando inúmeras obras que fazem sucesso até os dias atuais.</p>
