    <td> 
      <p>Fernando Antônio Nogueira Pessoa foi um dos mais importantes escritores e poetas do modernismo em Portugal. Nasceu em 13 de junho de 1888 na cidade de Lisboa (Portugal) e morreu, na mesma cidade, em 30 de novembro de 1935.</p>

<p>Fernando Pessoa foi morar, ainda na infância, na cidade de Durban (África do Sul), onde seu pai tornou-se cônsul. Neste país teve contato com a língua e literatura inglesa. Adulto, Fernando Pessoa trabalhou como tradutor técnico, publicando seus primeiro poemas em inglês.</p>

<p>Em 1905, retornou sozinho para Lisboa e, no ano seguinte, matriculou-se no Curso Superior de Letras. Porém, abandou o curso um ano depois. Pessoa passou a ter contato mais efetivo com a literatura portuguesa, principalmente Padre Antônio Vieira e Cesário Verde. Foi também influenciado pelos estudos filosóficos de Nietzsche e Schopenhauer. Recebeu também influências do simbolismo francês.</p>
<p>Em 1912, começou suas atividades como ensaísta e crítico literário, na revista Águia. A saúde do poeta português começou a apresentar complicações em 1935. Neste ano, foi hospitalizado com cólica hepática, provavelmente causada pelo consumo excessivo de bebida alcoólica. Sua morte prematura, aos 47 anos, provavelmente aconteceu em função destes problemas, pois apresentou cirrose hepática.</p>
<h4>O ortônimo e os heterônimos de Fernando Pessoa</h4>
<p>Fernando Pessoa usou em suas obras diversas autorias. Usou seu próprio nome (ortônimo) para assinar várias obras e pseudônimos (heterônimos) para assinar outras. Os heterônimos de Fernando Pessoa tinham personalidade própria e características literárias diferenciadas. São eles:</p>
<ul>
  <li><Strong>Álvaro de Campos </Strong></li>
  <p>Era um engenheiro português de educação inglesa. Influenciado pelo simbolismo e futurismo, apresentava certo niilismo em suas obras. </p>
<li><Strong>Ricardo Reis</Strong></li>
<p>Era um médico que escrevia suas obras com simetria e harmonia. O bucolismo estava presente em suas poesias. Era um defensor da monarquia e demonstrava grande interesse pela cultura latina.</p>
<li><Strong>Alberto Caeiro</Strong></li>
<p>Com uma formação educacional simples (apenas o primário), este heterônimo fazia poesias de forma simples, direta e concreta. Suas obras estão reunidas em Poemas Completos de Alberto Caeiro.
</p>
</ul>
