 <h1>Carlos Drummond de Andrade</h1>
 <table>
  <th>
    <td>
      <img src="img/carlos.jpg" width="400" height="500" class="foto">
    </td>
    <td>
      <p> Este consagrado poeta brasileiro nasceu em Itabira, Minas Gerais no ano de 1902. Tornou-se, pelo conjunto de sua obra, um dos principais representantes da literatura brasileira do século XX.
</p>

<p>Concretizou seus estudos em Belo Horizonte e, neste mesmo local, deu início a sua carreira de redator, na imprensa. Também trabalhou por vários anos como funcionário público.
</p>

<p>Em 1927, nasceu seu filho Carlos Flávio. No ano seguinte nasceu sua filha Maria Julieta.
</p>

<p>Seus poemas abordam assuntos do dia a dia, e contam com uma boa dose de pessimismo e ironia diante da vida. Em suas obras, há ainda uma permanente ligação com o meio e obras politizadas. Além das poesias, escreveu diversas crônicas e contos.</p>

<p>Os principais temas retratados nas poesias de Drummond são: conflito social, a família e os amigos, a existência humana, a visão sarcástica do mundo e das pessoas e as lembranças da terra natal.
</p>
<p>Dentre suas obras poéticas mais importantes destacam-se: Brejo das Almas, Sentimento do Mundo, José, Lição de Coisas, Viola de Bolso, Claro Enigma, Fazendeiro do Ar, A Vida Passada a Limpo e Novos Poemas.
</p>
<p>Talentoso também na prosa, tem suas prosas reunidas nos seguintes volumes: Confissões de Minas, Contos de Aprendiz, Passeios na Ilha e Fala Amendoeira.
</p>
<p>Na década de 1980 lançou as seguintes obras: A Paixão Medida, que contém 28 poemas inéditos; Caso do Vestido (1983); Corpo (1984); Amar se aprende amando (1985) e Poesia Errante (1988).</p>
<p>Faleceu, de problemas cardíacos, em 17 de agosto de 1987, no Rio de Janeiro, doze dias após a morte de sua filha Maria Julieta.</p>
    </td>
  </th>
