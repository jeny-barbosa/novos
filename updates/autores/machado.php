 <h1>Machado de Assis</h1>
 <table>
  <th>
    <td>
      <img src="img/machado.png" width="400" height="500" class="foto">
    </td>
    <td>
      <p>Joaquim Maria Machado de Assis é considerado um dos mais importantes escritores da literatura brasileira. Nasceu no Rio de Janeiro em 21/6/1839, filho de uma família muito pobre. Mulato e vítima de preconceito, perdeu na infância sua mãe e foi criado pela madrasta. Superou todas as dificuldades da época e tornou-se um grande escritor. Na infância, estudou numa escola pública durante o primário e aprendeu francês e latim. Trabalhou como aprendiz de tipógrafo, foi revisor e funcionário público.
</p>

<p>Publicou seu primeiro poema intitulado Ela, na revista Marmota Fluminense. Trabalhou como colaborador de algumas revistas e jornais do Rio de Janeiro. Foi um dos fundadores da Academia Brasileira de letras e seu primeiro presidente.
</p>

<p>Podemos dividir as obras de Machado de Assis em duas fases: Na primeira fase (fase romântica) os personagens de suas obras possuem características românticas, sendo o amor e os relacionamentos amorosos os principais temas de seus livros. Desta fase podemos destacar as seguintes obras: Ressurreição (1872), seu primeiro livro, A Mão e a Luva (1874), Helena (1876) e Iaiá Garcia (1878).
</p>

<p>Na Segunda Fase (fase realista), Machado de Assis abre espaços para as questões psicológicas dos personagens. É a fase em que o autor retrata muito bem as características do realismo literário. Machado de Assis faz uma análise profunda e realista do ser humano, destacando suas vontades, necessidades, defeitos e qualidades. Nesta fase destacam-se as seguintes obras: Memórias Póstumas de Brás Cubas (1881), Quincas Borba (1892), Dom Casmurro (1900) e Memorial de Aires (1908).</p>

<p>Machado de Assis também escreveu contos, tais como: Missa do Galo, O Espelho e O Alienista. Escreveu diversos poemas, crônicas sobre o cotidiano, peças de teatro, críticas literárias e teatrais.
</p>
<p>Machado de Assis morreu de câncer, em sua cidade natal, no ano de 1908.</p>
    </td>
