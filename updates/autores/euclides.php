<p>Euclides da Cunha nasceu no Rio de Janeiro, em 20 de janeiro de 1866 e morreu neste mesmo estado, em 1909, aos 43 anos de idade.</p>

<p>Era engenheiro, porém seu talento literário não passava despercebido, e, logo recebeu um convite para ser correspondente do jornal “O Estado de São Paulo”, este fato ocorreu durante o período da Guerra de Canudos.</p>

<p>Posteriormente, escreveu sobre esta revolta no livro: Os Sertões, obra que atingiu um grande sucesso. Nesta obra ele faz ainda uma análise brilhante da psicologia do sertanejo e de seus costumes. Escreveu também: Peru versus Bolívia, Contrastes e Confrontos e A Margem da História.</p>

<p>Sua vida privada foi repleta de contrariedades e grandes dificuldades financeiras. No ano de 1909 ele prestou concurso para o magistério. Apesar de ter sido aprovado ele não teve tempo de assumir o cargo, pois, foi assassinado pouco tempo depois. Sua obra é reconhecida até os dias de hoje e seus livros são lidos pelos apreciadores da literatura brasileira.
