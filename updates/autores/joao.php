    <td> 
      <p>João Paulo Barreto nasceu no Rio de Janeiro em 1881. Estudou com o pai, o professor Alfredo Coelho Barreto, e ainda adolescente ingressou na imprensa. Em 1899, estava entre os colaboradores do jornal Cidade do Rio, ao lado de José do Patrocínio e outros veteranos das letras e da política do país. Surgiu então o pseudônimo de João do Rio, com o qual o rapaz se tornou nacionalmente conhecido.</p>

<p>Muitos consideram João do Rio o primeiro jornalista brasileiro a assinar reportagens modernas, basicamente informativas, em vez de peças literárias. Algumas delas, publicadas em 1904 no jornal carioca Gazeta de Notícias, foram reunidas no livro As Religiões no Rio. A preocupação em registrar os mais diferentes aspectos do cotidiano carioca também transparece em seus contos e crônicas. Em alguns deles, pontilhados de expressões francesas, o autor retrata a sofisticação e a dissolução dos costumes da elite da capital, descrevendo festas dominadas pelas figuras sensuais das cortesãs. Em outros, porém, o erotismo é mais direto, manifestando-se nas ruas, entre a massa popular, durante o Carnaval. 
</p>
<p>Além de crônicas, contos e reportagens, João do Rio escreveu também o romance A Correspondência de uma Estação de Cura (1918) e várias peças teatrais. A de maior êxito foi A Bela Madame Vargas, encenada em 1912. Nesse momento, o escritor já integrava a Academia Brasileira de Letras, para a qual fora eleito em 1910. 
</p>

<p>João do Rio morreu em 1921, enquanto escrevia sua colaboração diária para o jornal A Pátria, fundado por ele em 1920. O velório foi realizado na própria redação.
</p>
