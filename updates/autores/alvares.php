    <td>
      <p> Manuel Antônio Álvares de Azevedo, nasceu aos 12 de setembro de 1831, em São Paulo. Matriculou-se no curso de Direito em 1848 e deu início à produção literária, ao passo que começou a sentir os primeiros sintomas de tuberculose.
</p>

<p>Alguns dizem que o autor teve uma vida boêmia e para outros, uma vida casta. Contudo, suas poesias mostram uma idéia fixa em sua própria morte, provavelmente por saber do estado de saúde em que se encontrava. Entretanto, o poeta também passou pela experiência da morte prematura do irmão e de seus colegas de faculdade.</p>

<p> Inspirado pela literatura de Lord Byron e Musset, Álvares de Azevedo impregnou suas poesias com ares sarcásticos e irônicos e com idéias de autodestruição, morte, dor e de uma visão de amor irreal e idealizado por donzelas virgens. Além disso, seus poemas de temáticas de frustração e sofrimento ganham um ar melancólico por lembranças da infância, da mãe e da irmã.</p>

<p>Álvares de Azevedo parecia viver uma dualidade de sentimentos que são transpassadas para sua literatura: ora é meigo e sentimental, ora é mordaz e trágico. Por ter o pessimismo como âncora de seus poemas, foi considerado o responsável pelo “mal do século”, caracterizado pelo sentimento melancólico e pelo desencanto.
</p>

<p>Em 1851, a idéia de que a morte era certeira em sua vida, começou a escrever cartas à mãe, à irmã e aos amigos certificando-os do seu inevitável destino.
</p>

<p>Sua temática voltada à morte pode ser considerada também como um refúgio, uma fuga da realidade conturbada que vivia e da relação com o mundo que o cercava, o qual lhe dava sensação de impotência.</p>
<p>O poeta desejou tanto a morte que morreu ainda jovem, aos 20 anos, a 25 de abril de 1852.
</p>
